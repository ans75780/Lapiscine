#include <unistd.h>

void	ft_print_comb(void)
{
	int		num;
	char	char_array[3];

	num = 0;
	while (num <= 789)
	{
		char_array[0] = '0' + num / 100;
		char_array[1] = '0' + (num / 10) % 10;
		char_array[2] = '0' + num % 10;
		if ((num / 100) < ((num / 10) % 10) && ((num / 10) % 10) < (num % 10))
		{
			write(1, &char_array, 3);
			if (num < 789)
				write(1, ", ", 2);
		}
		num++;
	}
}
